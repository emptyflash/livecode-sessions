await loadScript("https://cdn.statically.io/gl/metagrowing/extra-shaders-for-hydra/main/lib/lib-noise.js")

noise(1, 0.1, 2, 3)
	.modulate(warp(5, 0.5, 2, 3, 1))
  .thresh()
	.out(o0)
  warp(2, 0.1, 6, 3)
  	.out(o1)
osc(2, 0.5, 0.5)
	.rotate(() => 0.2 * time)
	.modulateScale(warp(2, 0.1, 1, 2, 0.5), 1)
  .saturate(0.3)
	.out(o2)
osc(1.5 * Math.PI, 0, 0.6)
	.modulate(warp(0.5, 0.1, 2, 3)
		.add(gradient(), -1), 1)
	.out(o3)
render()
